class UserEntity {
  final String uId;
  final String name;
  final String email;
  final String phone;
  final String image;
  final String address;
  final bool status;
  final String createdAt;
  final String updatedAt;

  const UserEntity({
    required this.uId,
    required this.name,
    required this.email,
    required this.phone,
    required this.image,
    required this.address,
    required this.status,
    required this.createdAt,
    required this.updatedAt,
  });
}



